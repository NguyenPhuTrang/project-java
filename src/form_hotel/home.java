    private void tb_danhSachPhongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_danhSachPhongMouseClicked
        // TODO add your handling code here:
        int row = tb_danhSachPhong.getSelectedRow();
        String RoomNumber = tb_danhSachPhong.getValueAt(row, 0).toString();
        String TypeRoom = tb_danhSachPhong.getValueAt(row, 1).toString();
        String Bed = tb_danhSachPhong.getValueAt(row, 2).toString();
        String Price = tb_danhSachPhong.getValueAt(row, 3).toString();
        String imgUrl = tb_danhSachPhong.getValueAt(row, 4).toString();
        String Status = tb_danhSachPhong.getValueAt(row, 5).toString();

        if (Status.toString().equals("Đóng cửa")) {
            JOptionPane.showMessageDialog(null, "Phòng " + RoomNumber + " hiện tại đang có người ở, Quý khách vui lòng chọn phòng khác !");
        } else {
            txt_soPhong.setText(RoomNumber);
            int i = 0;

            while (true) {
                String nameFromCbB = cb_loaiPhong.getItemAt(i).toString();
                if (nameFromCbB.equalsIgnoreCase(TypeRoom)) {
                    cb_loaiPhong.setSelectedIndex(i);
                    break;
                }
                i++;
            }

            while (true) {
                String nameFromCbB = cb_kieuGiuong.getItemAt(i).toString();
                if (nameFromCbB.equalsIgnoreCase(Bed)) {
                    cb_kieuGiuong.setSelectedIndex(i);
                    break;
                }
                i++;
            }

            txt_Gia.setText(Price + ".000 VND");
            new showroom(RoomNumber, TypeRoom, Bed, Price, imgUrl, Status).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_tb_danhSachPhongMouseClicked

    private void lb_SearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lb_SearchMouseClicked
        // TODO add your handling code here:
        String search = txt_Search.getText();
        ResultSet rs = Select.getData("select * from room where roomNumber = '" + search + "' or roomType like '" + search + "''%' ");
        DefaultTableModel model = (DefaultTableModel) tb_danhSachPhong.getModel();
        model.setRowCount(0);
        try {
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_lb_SearchMouseClicked

