    private void btn_datPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_datPhongActionPerformed
        // TODO add your handling code here:
        int id = 1;
        String name = txt_tenKhachHang.getText();
        String phoneNumber = txt_Sdt.getText();
        String nationality = txt_quocTich.getText();
        String gender = (String) cb_gioiTinh.getSelectedItem();
        String email = txt_Email.getText();
        String idProof = txt_MCMND.getText();
        String address = txt_diaChi.getText();
        String checkIn = txt_ngayDatPhong.getText();
        String bed = (String) cb_loaiGiuong2.getSelectedItem();
        String roomType = (String) cb_loaiPhong2.getSelectedItem();
        String roomNumber = (String) cb_soPhong2.getSelectedItem();
        String price = txt_Gia2.getText();

        String Query = "select max(id) from customer";
        if (name.equals("") || phoneNumber.equals("") || nationality.equals("") || email.equals("") || idProof.equals("") || address.equals("")) {
            JOptionPane.showMessageDialog(null, "Bạn cần điền đầy đủ thông tin trước khi đặt phòng.");
        } else {
            int a = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn đặt phòng không ? ", "Thông báo !", JOptionPane.YES_NO_OPTION);
            if (a == 0) {
                try {
                    ResultSet rs = Select.getData(Query);
                    while (rs.next()) {
                        id = rs.getInt(1);
                        id = id + 1;

                        if (!price.equals("")) {
                            Query = "update room set status = 'Đóng cửa' where roomNumber = '" + roomNumber + "'";
                            InsertUpdateDelete.setData(Query, "");
                            Query = "insert into customer(id, name , phoneNumber, nationality, gender , email , idProof, address, checkIn, roomNumber, bed, roomType, pricePerDay) values(" + id + ", '" + name + "', '" + phoneNumber + "', '" + nationality + "', '" + gender + "', '" + email + "', '" + idProof + "','" + address + "', '" + checkIn + "', '" + roomNumber + "', '" + bed + "','" + roomType + "', '" + price + "')";
                            InsertUpdateDelete.setData(Query, " Bạn đặt phòng thành công !");
                            setVisible(false);
                            new home().setVisible(true);
                        }
                    }
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_btn_datPhongActionPerformed

