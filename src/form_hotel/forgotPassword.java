    private void jLabel21MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel21MouseClicked
        // TODO add your handling code here:
       int check = 0;
        email = txt_search.getText();
        if(email.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Email không được bỏ trống");
        }
        else 
        {
            ResultSet rs = Select.getData("select * from users where email='"+email+"'");
            try {
                if(rs.next())
                {
                    check = 1;
                    txt_cauHoiBaoMat.setEditable(false);
                    txt_search.setEditable(false);
                    txt_cauHoiBaoMat.setText(rs.getString(4));
                }
            } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
            }
            if(check ==0)
            {
                    JOptionPane.showMessageDialog(null,"Email khong chinh xac");
            }
        }
    }//GEN-LAST:event_jLabel21MouseClicked

