    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int check = 0;
        String security = txt_cauHoiBaoMat.getText();
        String answer = txt_cauTraLoi.getText();
        String newPassword = txt_matKhauMoi.getText();
        if(answer.equals("")|| newPassword.equals(""))
        {
            check = 1;
            JOptionPane.showMessageDialog(null, "Yêu cầu nhập đầy đủ thông tin");
        }
        else 
        {
            ResultSet rs = Select .getData("select * from users where email='"+email+"' and securityQuestion = '"+security+"' and answer = '"+answer+"'");
            try {
                if(rs.next())
                {
                    check=1;
                    InsertUpdateDelete.setData("update users set password = '"+newPassword+"' where email = '"+email+"'","Change password succesfull");
                    setVisible(false);
                    new forgotPassword().setVisible(true);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        if(check==0)
            JOptionPane.showMessageDialog(null, "Câu trả lời không đúng");
    }//GEN-LAST:event_jButton2ActionPerformed

